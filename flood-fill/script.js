function addEventListeners() {
  document.querySelector("#stack-btn").addEventListener("click", () => floodFillStack(0, 0));
  document.querySelector("#recursive-btn").addEventListener("click", () => floodFill(0, 0));
  document.querySelector("#queue-btn").addEventListener("click", () => floodFillQueue(0, 0));
  document.querySelector("#reset-btn").addEventListener("click", resetGame);
}

function resetGame() {
  console.log("resetting game");
  for (let i = 0; i < gridArray.length; i++) {
    for (let j = 0; j < gridArray[0].length; j++) {
      if (gridArray[i][j] != 1) {
        gridArray[i][j] = 0;
      }
    }
  }
  if (stackTimer) {
    clearTimeout(stackTimer);
  }
  if (queueTimer) {
    clearTimeout(queueTimer);
  }
  console.log(gridArray);
}

function loop() {
  requestAnimationFrame(loop);

  updateDisplay();
}


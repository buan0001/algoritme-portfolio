function buildGrid(width, height) {
  const grid = document.querySelector("#grid");

  grid.innerHTML = "";

  for (let y = 0; y < height; y++) {
    for (let x = 0; x < width; x++) {
      const cell = document.createElement("div");
      cell.classList.add("cell");
      grid.append(cell);
    }
  }

  grid.style.setProperty("--GRID-WIDTH", width);
  grid.style.setProperty("--GRID-HEIGHT", height);
}

function updateDisplay() {
  const cells = document.querySelectorAll("#grid .cell");
  for (let y = 0; y < GRID_HEIGHT; y++) {
    for (let x = 0; x < GRID_WIDTH; x++) {
      const cell = cells[y * GRID_WIDTH + x];
      switch (gridArray[y][x]) {
        case 1:
          cell.classList.add("image");
          break;
        case 2:
          cell.classList.remove("draw");
          cell.classList.add("set");
          break;
        case 3:
          cell.classList.add("draw");
          break;
        default:
          cell.classList.remove("image", "set", "draw");
          break;
      }
    }
  }
}

function set(x, y) {
  if (y < gridArray.length && x < gridArray[y].length) {
    gridArray[y][x] = 2;
  }
}

function clear(x, y) {
  if (y < gridArray.length && x < gridArray[y].length) {
    gridArray[y][x] = 0;
  }
}

function draw(x, y) {
  if (y < gridArray.length && x < gridArray[y].length) {
    gridArray[y][x] = 3;
  }
}

/*
  The weird "Inside" function as defined by the Flood-fill algorithm
  really just returns true if the pixel isn't already colored in ...
 */
function inside(x, y) {
  if (x >= 0 && y >= 0 && y < gridArray.length && x < gridArray[y].length) {
    return gridArray[y][x] == 0;
  }

  return false;
}

// With queue
const que = new Queue();
let queueTimer;
function floodFillQueue(x, y) {
  que.enqueue({ x, y });

  function processStack(x, y) {
    if (x != undefined && y != undefined) {
      set(x, y);
    }
    if (que.peek()) {
      const topNode = que.dequeue();
      const x = topNode.data.x;
      const y = topNode.data.y;
      if (inside(x, y)) {
        draw(x, y);
        // for (let i = -1; i <= 1; i++) {
        //   for (let j = -1; j <= 1; j++) {
        //     if (j !== 0 || i !== 0) {
        //       que.enqueue({ x: x + i, y: y + j });
        //     }
        //   }
        // }
        que.enqueue({ x: x - 1, y });
        que.enqueue({ x: x + 1, y });
        que.enqueue({ x, y: y - 1 });
        que.enqueue({ x, y: y + 1 });
        // que.enqueue({ x: x + 1, y: y + 1 });
        // que.enqueue({ x: x - 1, y: y - 1 });
        // que.enqueue({ x: x - 1, y: y + 1 });
        // que.enqueue({ x: x + 1, y: y - 1 });
        queueTimer = setTimeout(() => processStack(x, y), 10);
      } else {
        processStack();
      }
    } else {
      console.log("Done");
    }
  }
  processStack();
}


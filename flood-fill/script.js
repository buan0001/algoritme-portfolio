// Classic recursive solution
function floodFill(x, y) {
  if (!inside(x, y)) {
    console.log("not inside");

    return;
  }

  set(x, y);
  setTimeout(() => {
    floodFill(x - 1, y);
    floodFill(x + 1, y);
    floodFill(x, y - 1);
    floodFill(x, y + 1);
  }, 50);

  return;
}
